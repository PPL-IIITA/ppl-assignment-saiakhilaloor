class gifts:
    def __init__(self,price,value,category):
        self.price = price
        self.value = value
        self.category = category

class essentialgifts(gifts):
    def __init__(self):
        self.category = "Essential"

class luxurygifts(gifts):
    def __init__(self):
        self.category = "Luxury"

class utilitygifts(gifts):
    def __init__(self):
        self.category = "Utility"































































































































































































































































































































































































































































































